package Javalearning;

public class WhileLoops {
    public static void main(String[] args) {
        /*While loop-print 1 to 10*/

        int i = 0;
        while (i <= 10) {
            System.out.println(i);
            i++;

            /*print 10 to 1*/

            int j = 10;

            while (j >0)
            {
                System.out.println(j);
                j--;
            }
        }
    }
}